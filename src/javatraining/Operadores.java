package javatraining;

public class Operadores {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 25;
        int d = 25;
        System.out.println("a + b = " + (a + b) ); //suma
        System.out.println("a - b = " + (a - b) ); //resta
        System.out.println("a * b = " + (a * b) ); //multiplicación
        System.out.println("b / a = " + (b / a) ); //división
        System.out.println("b % a = " + (b % a) ); //módulo
        System.out.println("c % a = " + (c % a) ); //módulo
        System.out.println("a++   = " +  (a++) ); //incremento
        System.out.println("a--   = " +  (a--) ); //decremento
        // Check the difference in d++ and ++d
        System.out.println("d++   = " +  (d++) ); //incremento
        System.out.println("++d   = " +  (++d) ); //incremento

        // logicos, relacion, comparacion, booleanos

        /* Operadores lógicos
        * > mayor que
        * < menor que
        * == igual que
        * += asignación
        * != diferente que
        * >= mayor o igual que
        * <= menor o igual que
        * && and
        * || or
        * ! not
        * */

    }
}
